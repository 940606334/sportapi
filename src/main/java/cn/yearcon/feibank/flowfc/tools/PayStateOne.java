package cn.yearcon.feibank.flowfc.tools;

public class PayStateOne {
	private String channelOrderId;
	private String orderId;
	private String orderStatus;
	private String orderRspMsg;
	private String orderFinishTime;
	public String getChannelOrderId() {
		return channelOrderId;
	}
	public void setChannelOrderId(String channelOrderId) {
		this.channelOrderId = channelOrderId;
	}
	public String getOrderId() {
		return orderId;
	}
	public void setOrderId(String orderId) {
		this.orderId = orderId;
	}
	public String getOrderStatus() {
		return orderStatus;
	}
	public void setOrderStatus(String orderStatus) {
		this.orderStatus = orderStatus;
	}
	public String getOrderRspMsg() {
		return orderRspMsg;
	}
	public void setOrderRspMsg(String orderRspMsg) {
		this.orderRspMsg = orderRspMsg;
	}
	public String getOrderFinishTime() {
		return orderFinishTime;
	}
	public void setOrderFinishTime(String orderFinishTime) {
		this.orderFinishTime = orderFinishTime;
	}
	
	
}
